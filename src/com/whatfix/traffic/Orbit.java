package com.whatfix.traffic;

public class Orbit {
    private float distance;
    private int noOfCraters;
    private String orbitName;

    public String getOrbitName() {
        return orbitName;
    }

    public void setOrbitName(String orbitName) {
        this.orbitName = orbitName;
    }

    public float getDistance() {
        return distance;
    }

    public void setDistance(int distance) {
        this.distance = distance;
    }

    public int getNoOfCraters() {
        return noOfCraters;
    }

    public void setNoOfCraters(int noOfCraters) {
        this.noOfCraters = noOfCraters;
    }
}
